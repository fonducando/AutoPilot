void Setup_Aileron_PID ()
{
	Aileron_PID = pid_create(&Apidctrl, &Current_Bank, &AOutput, &Desired_Bank, Akp, Aki, Akd);
	// Set controler output limits from 0 to 200
	pid_limits(Aileron_PID, 0, 200);
	// Allow PID to compute and change output
	pid_manual(Aileron_PID);
}

void Send_Elevator_Steps (int64_t StepNum)
{

	//Set Stepper Direction
	if (StepNum < 0)
	{
	    Elevator_Direction_Set(StepperBackward);
	    StepNum = abs(StepNum);
	}

	if (StepNum >= 0)
	{
		Elevator_Direction_Set(StepperForward);
		StepNum = abs(StepNum);
	}
    //Complete Steps
	for(int64_t x = 0; x < StepNum; x++)
	{
		HAL_GPIO_WritePin(Elevator_Step_GPIO_Port, Elevator_Step_Pin, 1);
		HAL_Delay(Step_Delay / 2);
		HAL_GPIO_WritePin(GPIOD, Elevator_Step_Pin, 0);
		HAL_Delay(Step_Delay / 2);
	}
}

void Send_Airleron_Steps(int64_t StepNum)
{
	//Set Stepper Direction
	if (StepNum < 0)
	{
	    Aileron_Direction_Set(StepperBackward);
	    StepNum = abs(StepNum);
	}

	if (StepNum >= 0)
	{
		Aileron_Direction_Set(StepperForward);
		StepNum = abs(StepNum);
	}
	//Complete Steps
	for(int64_t x = 0; x < StepNum; x++)
	{
		HAL_GPIO_WritePin(Aileron_Step_GPIO_Port, Aileron_Step_Pin, 1);
		HAL_Delay(Step_Delay / 2);
		HAL_GPIO_WritePin(GPIOD, Aileron_Step_Pin, 0);
		HAL_Delay(Step_Delay / 2);
	}
}

